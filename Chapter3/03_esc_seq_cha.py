# \n is use to print line in new line without pressing in actual code which ultimately give you an error. e.g:- 
a = "I like apples\nBut not bananas"
print(a)

# \t is use to give tab space in between characters
b = ("I like to play cricket\tAlso football")
print(b)

# \' or \" is use to give to give quotes for words in sentences without interfering with actual code of python.
c = " Rahul said\n \"Can you be my friend\"?"
print(c)

# \\ is use to insert a single \ in between words.

d = "i wish to be the richest man of the world\\with the kindest heart"
print(d)