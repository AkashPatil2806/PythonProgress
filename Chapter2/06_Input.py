# 1)Input Function is used to take input from the user. The input() function reads a line from the input (usually from the user), converts it into a string, and returns it.
# example
a = input("Enter Your Name;")
b = input("Enter Your Surname;")
print("Your full name is:", a, b)

c = int(input("Enter First Number:"))
d = int(input("Enter Second Number:"))
print("The sum of the two numbers is:", c + d)
# THE int() FUNCTION IS USED TO CONVERT THE INPUT STRING TO INTEGER SO THAT WE CAN PERFORM ARITHMETIC OPERATIONS ON IT.