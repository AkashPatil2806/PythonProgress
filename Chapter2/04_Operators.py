#There are basically 4 types of operators in Python: arithmetic, comparison, logical, and assignment operators. Each operator serves a different purpose and can be used to perform various operations on data.

# 1) Arithmetic Operators: These operators are used to perform mathematical
# operations on numerical values. The basic arithmetic operators in Python are: +, -, *, /, %, **, and //.
# example 

a = 6+7
print (a)

b = 6-7
print (b)

c = 6*7
print (c)

d = 6/7
print (d)

# 2) Comparison Operators: These operators are used to compare two values and return a boolean result (True or False). 
# he basic comparison operators in Python are: ==, !=, >, <, >=, and <=.

a = 5
b = 10
c = a == b
print (c)

d = a != b
print (d)

e = a > b
print (e)


f = a < b
print (f)


g = a >= b
print (g)

h = a <= b
print (h)

# 3) Logical Operators: These operators are used to combine multiple boolean expressions and return a boolean result. The basic logical operators in Python are: and, or, and not.

i = True
j = False
k = i and j
print(k)

l = i or j
print (l)

m = not i
print (m)

#4) Assignment Operators: These operators are used to assign values to variables.
#  The basic assignment operators in Python are: =, +=, -=, *=, /=, %=, **=, and //=.

a = 5
print(a)

a += 3
print(a)

a -= 2
print(a)

a *= 4
print(a)

a /= 2
print(a)