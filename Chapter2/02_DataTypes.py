#  There are 4 types of data types in Python: integers, floats, strings, and booleans.
# Each data type serves a different purpose and can be used in various ways to store and manipulate data.
# 1) Integers: These are whole numbers without a decimal point. They can be positive or negative. For example, 5, -3, and 42 are all integers. Integers are commonly used for counting, indexing, and performing arithmetic operations.
a = 10
b = -5 

# 2) Floats: These are numbers that have a decimal point. They can also be positive or negative. For example, 3.14, -0.5, and 2.0 are all floats. Floats are often used in calculations that require precision, such as measurements or financial calculations.
c = 3.14
d = -0.5
e = 2.0

# 3) Strings: These are sequences of characters enclosed in single or double quotes. For example, "Hello", 'Python', and "123" are all strings. Strings are used to represent text and can be manipulated using various string methods.3
f = "Akash"

# 4) Booleans: These are data types that can have one of two values: True or False. Booleans are often used in conditional statements and logical operations to control the flow of a program. For example, the expression (5 > 3) evaluates to True, while (2 == 3) evaluates to False.
g = True
h = False

# 5) None: This is a special data type that represents the absence of a value or a null value. It is often used to indicate that a variable has not been assigned a value or that a function does not return anything. For example, if a function does not have a return statement, it will return None by default.
i = None
